function toneForActionClassification(classification: ReportSnapshot["decisionIntelligenceBrief"]["executiveActionBanner"]["classification"]): EnterpriseStatusTone {
  if (classification === "Excellent" || classification === "Healthy but Optimize") return "green";
  if (classification === "Action Recommended") return "amber";
  if (classification === "High Risk" || classification === "Critical Attention Required") return "red";
  return "gray";
}

