export async function renderCustomerDecisionPdf(decision: AiceDecisionObject): Promise<Buffer> {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 44, size: "A4", bufferPages: true });
    const chunks: Buffer[] = [];
    doc.on("data", (chunk: Buffer) => chunks.push(chunk));
    doc.on("error", reject);
    doc.on("end", () => resolve(Buffer.concat(chunks)));

    writeCustomerDecisionHeader(doc, decision);
    writeCustomerDecisionBoundary(doc, decision);
    writeCustomerEvidenceSummary(doc, decision);
    writeCustomerBusinessBoundary(doc, decision);
    writeCustomerRecommendation(doc, decision);
    writeCustomerDecisionFooter(doc);
    doc.end();
  });
}

function writeCustomerDecisionHeader(doc: PDFKit.PDFDocument, decision: AiceDecisionObject): void {
  doc
    .fillColor("#17201d")
    .fontSize(22)
    .text("SYSTOLAB Customer Decision Report")
    .fontSize(10)
    .fillColor("#52605a")
    .text(`Target: ${decision.target}`)
    .text("Customer Intelligence Plane")
    .moveDown(1);

  doc
    .fontSize(24)
    .fillColor(decision.confidence_score >= 75 ? "#16845b" : decision.confidence_score >= 45 ? "#b87500" : "#b3261e")
    .text(`${decision.confidence_score}% Confidence`)
    .fontSize(10)
    .fillColor("#17201d")
    .text(`Risk level: ${toPdfTitleCase(decision.risk_level)}`)
    .text(`Coverage: ${decision.coverage_score}% ${decision.evidence_summary.coverage_status}`)
    .moveDown(0.8);
}

function writeCustomerDecisionBoundary(doc: PDFKit.PDFDocument, decision: AiceDecisionObject): void {
  sectionTitle(doc, "Assessment Boundary");
  doc
    .fontSize(9)
    .fillColor("#17201d")
    .text(`Access Restriction Detected: ${decision.access_restriction_detected ? "Yes" : "No"}`)
    .fillColor("#52605a")
    .text(decision.assessment_limitation || "Assessment was limited to validated evidence collected during this scan.")
    .moveDown(0.8);
}

function writeCustomerEvidenceSummary(doc: PDFKit.PDFDocument, decision: AiceDecisionObject): void {
  sectionTitle(doc, "Evidence Summary");
  doc
    .fontSize(9)
    .fillColor("#17201d")
    .text(decision.evidence_summary.overview)
    .fillColor("#52605a")
    .text(`Sampled pages: ${decision.evidence_summary.sampled_pages}`)
    .text(`Strongest signal: ${decision.evidence_summary.strongest_business_signal}`)
    .text(`Weakest signal: ${decision.evidence_summary.weakest_business_signal}`)
    .moveDown(0.6);

  for (const item of decision.evidence_heatmap_summary) {
    doc
      .fontSize(8)
      .fillColor("#17201d")
      .text(`${item.area}: ${toPdfTitleCase(item.coverage)}`)
      .fillColor("#52605a")
      .text(item.business_meaning)
      .moveDown(0.25);
  }
  doc.moveDown(0.4);
}

function writeCustomerBusinessBoundary(doc: PDFKit.PDFDocument, decision: AiceDecisionObject): void {
  sectionTitle(doc, "Business Impact Boundary");
  doc
    .fontSize(9)
    .fillColor("#17201d")
    .text(`Impact: ${decision.impact}`)
    .text(`Opportunity range: ${formatPdfDecisionRange(decision.revenue_impact_range)}`)
    .text(`Time sensitivity: ${decision.time_sensitivity}`)
    .text(`Action window: ${decision.recommended_action_window}`)
    .fillColor("#52605a")
    .text(decision.if_not_fixed_outcome)
    .moveDown(0.8);
}

function writeCustomerRecommendation(doc: PDFKit.PDFDocument, decision: AiceDecisionObject): void {
  sectionTitle(doc, "Recommended Action");
  doc.fontSize(10).fillColor("#17201d").text(decision.final_recommendation).moveDown(1);
}

function writeCustomerDecisionFooter(doc: PDFKit.PDFDocument): void {
  const pages = doc.bufferedPageRange();
  for (let index = 0; index < pages.count; index += 1) {
    doc.switchToPage(index);
    doc
      .fontSize(8)
      .fillColor("#7b8580")
      .text("Generated by SYSTOLAB Intelligence Platform. Conclusions are limited to validated evidence collected during the current scan.", 44, doc.page.height - 42, {
        align: "center"
      });
  }
}

function toPdfTitleCase(value: string): string {
  return value
    .toLowerCase()
    .replaceAll("_", " ")
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

function formatPdfDecisionRange(range: AiceDecisionObject["revenue_impact_range"]): string {
  if (range.low === 0 && range.high === 0) return range.label;
  const formatter = new Intl.NumberFormat("en-US", { maximumFractionDigits: 0 });
  return `${range.label}: ${formatter.format(range.low)}-${formatter.format(range.high)} ${range.unit.replaceAll("_", " ")}`;
}

