function writeEnterpriseBox(
  doc: PDFKit.PDFDocument,
  report: ReportSnapshot,
  title: string,
  tone: EnterpriseStatusTone,
  minHeight: number,
  draw: () => void,
  borderWidth = 1
): void {
  ensureEnterpriseSpace(doc, report, minHeight);
  const x = doc.page.margins.left;
  const width = enterpriseWidth(doc);
  const y = doc.y;
  const headerHeight = 30;
  const color = colorForTone(tone);

  doc.rect(x, y, width, headerHeight).fillColor(tintForTone(tone)).fill();
  doc.rect(x, y, width, headerHeight).strokeColor(ENTERPRISE_PDF.line).lineWidth(1).stroke();
  doc
    .font("Helvetica-Bold")
    .fontSize(12)
    .fillColor(ENTERPRISE_PDF.ink)
    .text(title, x + 12, y + 9, { width: width - 24 });
  doc.moveTo(x, y + headerHeight).lineTo(x + width, y + headerHeight).strokeColor(color).lineWidth(borderWidth).stroke();

  doc.y = y + headerHeight + 12;
  draw();
  doc.y += 12;
  const height = Math.max(doc.y - y, minHeight);
  doc.rect(x, y, width, height).strokeColor(color).lineWidth(borderWidth).stroke();
  doc.y = y + height + 16;
}

function writeEnterpriseTableRow(doc: PDFKit.PDFDocument, label: string, value: string, index: number): void {
  const x = enterpriseInnerX(doc);
  const width = enterpriseInnerWidth(doc);
  const labelWidth = 142;
  const valueWidth = width - labelWidth - 16;
  const cleanValue = pdfCleanText(value);
  const rowHeight = Math.max(24, doc.heightOfString(cleanValue, { width: valueWidth }) + 12);
  if (index % 2 === 0) doc.rect(x, doc.y, width, rowHeight).fillColor(ENTERPRISE_PDF.surface).fill();
  doc
    .font("Helvetica-Bold")
    .fontSize(8.5)
    .fillColor(ENTERPRISE_PDF.muted)
    .text(label, x + 8, doc.y + 7, { width: labelWidth })
    .font("Helvetica")
    .fillColor(ENTERPRISE_PDF.ink)
    .text(cleanValue, x + labelWidth + 12, doc.y + 7, { width: valueWidth });
  doc.y += rowHeight;
}

function writeMetricGrid(doc: PDFKit.PDFDocument, metrics: Array<[string, string, EnterpriseStatusTone]>): void {
  const x = enterpriseInnerX(doc);
  const width = enterpriseInnerWidth(doc);
  const gap = 10;
  const columns = metrics.length >= 4 ? 2 : metrics.length;
  const cardWidth = (width - gap * (columns - 1)) / columns;
  const cardHeight = 54;
  let y = doc.y;
  metrics.forEach(([label, value, tone], index) => {
    const col = index % columns;
    if (index > 0 && col === 0) y += cardHeight + gap;
    drawMetricMiniCard(doc, x + col * (cardWidth + gap), y, cardWidth, label, value, tone);
  });
  doc.y = y + cardHeight;
}

function drawMetricMiniCard(
  doc: PDFKit.PDFDocument,
  x: number,
  y: number,
  width: number,
  label: string,
  value: string,
  tone: EnterpriseStatusTone
): void {
  doc.roundedRect(x, y, width, 54, 4).fillColor(ENTERPRISE_PDF.white).fill().strokeColor(ENTERPRISE_PDF.line).lineWidth(1).stroke();
  doc.rect(x, y, 4, 54).fillColor(colorForTone(tone)).fill();
  doc
    .font("Helvetica-Bold")
    .fontSize(7.2)
    .fillColor(ENTERPRISE_PDF.muted)
    .text(label.toUpperCase(), x + 10, y + 8, { width: width - 18, lineBreak: false })
    .font("Helvetica")
    .fontSize(8.4)
    .fillColor(ENTERPRISE_PDF.ink)
    .text(pdfCleanText(value), x + 10, y + 23, { width: width - 18 });
}

