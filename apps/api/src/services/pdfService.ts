function writeEnterpriseReportHeader(doc: PDFKit.PDFDocument, report: ReportSnapshot): void {
  const left = doc.page.margins.left;
  const width = enterpriseWidth(doc);
  const top = doc.page.margins.top;

  doc
    .font("Helvetica-Bold")
    .fontSize(16)
    .fillColor(ENTERPRISE_PDF.ink)
    .text("SYSTOLAB Revenue Health Diagnosis", left, top, { width: width - 90 })
    .font("Helvetica")
    .fontSize(9)
    .fillColor(ENTERPRISE_PDF.muted)
    .text("Generated by SYSTOLAB Intelligence Platform.", left, top + 22, { width: width - 90 })
    .fontSize(8)
    .text(`Generated: ${new Date().toISOString()}`, left, top + 35, { width: width - 90 });

  doc.moveTo(left, top + 50).lineTo(left + width, top + 50).strokeColor(ENTERPRISE_PDF.line).lineWidth(1).stroke();

  const barY = top + 64;
  const barHeight = 54;
  doc.roundedRect(left, barY, width, barHeight, 4).fillColor(ENTERPRISE_PDF.surface).fill().strokeColor(ENTERPRISE_PDF.line).lineWidth(1).stroke();

  const metadata = [
    ["Snapshot ID", report.snapshotId],
    ["Target URL", report.targetUrl],
    ["Captured", report.createdAt],
    ["Coverage", report.scanCoverage?.coverageLabel ?? "Not Available"]
  ] as const;
  const columnWidth = width / metadata.length;
  metadata.forEach(([label, value], index) => {
    const x = left + columnWidth * index;
    if (index > 0) {
      doc.moveTo(x, barY + 10).lineTo(x, barY + barHeight - 10).strokeColor(ENTERPRISE_PDF.line).lineWidth(1).stroke();
    }
    doc
      .font("Helvetica-Bold")
      .fontSize(7.5)
      .fillColor(ENTERPRISE_PDF.muted)
      .text(label.toUpperCase(), x + 10, barY + 10, { width: columnWidth - 20, lineBreak: false })
      .font("Helvetica")
      .fontSize(8.2)
      .fillColor(ENTERPRISE_PDF.ink)
      .text(pdfCleanText(value), x + 10, barY + 26, { width: columnWidth - 20 });
  });

  doc.y = barY + barHeight + 18;
}

