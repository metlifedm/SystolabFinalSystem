function pdfDecisionBrief(report: ReportSnapshot): ReportSnapshot["decisionIntelligenceBrief"] {
  const candidate = (report as Partial<ReportSnapshot>).decisionIntelligenceBrief;
  if (
    candidate?.executiveVerdict &&
    candidate.executiveActionBanner &&
    candidate.executiveDecisionMatrix &&
    Array.isArray(candidate.actionPlan) &&
    candidate.whyThisMatters &&
    candidate.competitivePositionAnalysis &&
    candidate.executiveReliabilityPanel
  ) {
    return candidate;
  }

  const score = typeof report.oss?.score === "number" && report.oss.scoringStatus !== "not_scored" ? report.oss.score : null;
  if (score === null) return pdfUnavailableDecisionBrief(report);

  const dimensions = report.dimensions ?? [];
  const weakest = [...dimensions].sort((a, b) => a.score - b.score)[0];
  const strongest = [...dimensions].sort((a, b) => b.score - a.score).slice(0, 3);
  const recommendations = report.recommendationEngine?.recommendations ?? [];
  const firstAction = recommendations[0]?.action ?? report.actionFirstPanel?.items?.[0]?.executableFix ?? report.actionFirstPanel?.fallbackAction ?? "Review validated evidence before selecting the first structural change.";
  const coverage = report.evidenceCoverageSummary as Partial<ReportSnapshot["evidenceCoverageSummary"]> | undefined;
  const sampledPages = coverage?.totalPagesSampled ?? report.scanCoverage?.sampledPages ?? 0;
  const evidenceCount = coverage?.totalEvidenceObjects ?? report.evidenceObjects?.length ?? 0;
  const benchmarkStatus = pdfBenchmarkStatus(report);
  const competitorStatus = pdfCompetitorStatus(report);
  const confidenceScore = report.confidenceEngine?.overallConfidenceScore ?? pdfAverageConfidence(report.confidenceLayer ?? []);

  return {
    executiveVerdict: {
      currentSituation: `The website is scored at OSS ${score}/100 with ${(report.businessRiskStatus?.level ?? "not assessed").toLowerCase()}.`,
      seriousness: report.businessRiskStatus?.explanation ?? "Risk interpretation is limited to validated evidence.",
      firstAction,
      urgency: pdfTimeSensitivity(score),
      likelyBusinessImpact: pdfBusinessImpact(report, evidenceCount),
      evidenceBasis: `${sampledPages} sampled page${sampledPages === 1 ? "" : "s"} and ${evidenceCount} validated evidence object${evidenceCount === 1 ? "" : "s"} support this brief.`
    },
    executiveActionBanner: {
      classification: pdfClassification(score),
      message: weakest ? `Primary validated focus: ${weakest.label} at ${weakest.score}/100.` : "No single validated structural constraint was detected.",
      urgency: pdfTimeSensitivity(score)
    },
    executiveDecisionMatrix: {
      executiveDecisionScore: score,
      riskLevel: pdfRiskLevel(report.businessRiskStatus?.classification ?? "UNKNOWN"),
      executivePriority: pdfPriority(score),
      timeSensitivity: pdfTimeSensitivity(score),
      competitivePosition: benchmarkStatus,
      primaryBusinessConstraint: weakest ? `${weakest.label} is the lowest validated dimension at ${weakest.score}/100.` : report.businessRiskStatus?.primaryRiskDriver ?? "No primary constraint was validated.",
      potentialBusinessImpact: pdfBusinessImpact(report, evidenceCount),
      ifNotAddressedOutcome: report.systemVerdict?.businessConsequence ?? report.businessRiskStatus?.primaryRiskDriver ?? "No outcome projection was generated.",
      recommendedNextAction: firstAction
    },
    actionPlan: pdfActionPlan(report, weakest, firstAction, confidenceScore),
    whyThisMatters: {
      overallCondition: `The assessment is based on current observable website structure and produced OSS ${score}/100.`,
      strongestValidatedDimensions: strongest.map((dimension) => `${dimension.label} (${dimension.score}/100)`),
      weakestValidatedDimension: weakest ? `${weakest.label} (${weakest.score}/100)` : "No weak dimension was validated.",
      businessSignificance: report.systemVerdict?.businessConsequence ?? report.businessRiskStatus?.primaryRiskDriver ?? "Business significance is limited to validated evidence."
    },
    competitivePositionAnalysis: {
      summary: pdfCompetitiveSummary(benchmarkStatus, competitorStatus, report),
      benchmarkStatus,
      competitorStatus,
      dimensionPositions: (report.marketReadinessPosition?.positions ?? []).map((position) => ({
        dimension: position.dimension,
        dimensionLabel: position.dimensionLabel,
        position: position.position,
        confidenceScore: report.marketReadinessPosition?.comparativeConfidenceScore ?? 0,
        evidenceIds: position.evidenceIds
      }))
    },
    executiveReliabilityPanel: {
      evidenceCoverage: `${sampledPages} sampled page${sampledPages === 1 ? "" : "s"}, ${evidenceCount} validated evidence object${evidenceCount === 1 ? "" : "s"}.`,
      crawlCoverage: report.scanCoverage?.coverageLabel ?? "Not Available",
      assessmentConfidence: `${confidenceScore}% (${pdfConfidenceLevel(confidenceScore)})`,
      benchmarkConfidence: report.benchmarkContext?.comparativeConfidenceScore > 0 ? `${report.benchmarkContext.comparativeConfidenceScore}% (${report.benchmarkContext.status.replaceAll("_", " ")})` : "Not available",
      assessmentTrustSignals: pdfTrustSignalLabel(report),
      overallReportReliability: pdfConfidenceLevel(Math.min(confidenceScore, pdfCoverageReliability(sampledPages, evidenceCount))),
      limitations: pdfReliabilityLimitations(report)
    }
  };
}

