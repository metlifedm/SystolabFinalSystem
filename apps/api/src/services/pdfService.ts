async function writeScreenshotEvidence(
  doc: PDFKit.PDFDocument,
  report: ReportSnapshot,
  getArtifact?: (artifactId: string) => Promise<Buffer | null>
): Promise<void> {
  const screenshotEvidence = report.evidenceObjects.filter((ev) => ev.screenshotRef);
  if (!screenshotEvidence.length || !getArtifact) return;

  doc.addPage();
  sectionTitle(doc, "Screenshot Evidence");
  doc.fontSize(8).fillColor("#52605a")
    .text("The following screenshots were captured during headless browser rendering and are linked to specific evidence objects.")
    .moveDown(0.6);

  for (const ev of screenshotEvidence) {
    if (!ev.screenshotRef) continue;

    doc
      .fontSize(8)
      .fillColor("#17201d")
      .text(`${ev.evidenceId} — ${ev.normalizedInput.signalKey}`)
      .fillColor("#52605a")
      .text(`Page: ${ev.url}`)
      .text(`Signal: ${ev.rawValue}`)
      .text(`Render state: ${ev.renderState ?? "not_rendered"}`)
      .text(`Verification: ${ev.renderVerification ?? "not available"}`)
      .moveDown(0.3);

    try {
      const imageBuffer = await getArtifact(ev.screenshotRef);
      if (imageBuffer) {
        const remainingHeight = doc.page.height - doc.page.margins.bottom - doc.y;
        const imgHeight = Math.min(220, remainingHeight - 20);
        if (imgHeight < 40) doc.addPage();
        doc.image(imageBuffer, { fit: [doc.page.width - doc.page.margins.left - doc.page.margins.right, imgHeight] });
        doc.moveDown(0.5);
      } else {
        doc.fillColor("#a0afa8").text("[Screenshot not available]").moveDown(0.3);
      }
    } catch {
      doc.fillColor("#a0afa8").text("[Screenshot could not be loaded]").moveDown(0.3);
    }
  }
}

